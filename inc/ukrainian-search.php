/**
 * Apply typo correction to a search term (e.g. double letters).
 * "коррва" → ["коррва", "корова"]
 *
 * @param string $term Search term in Ukrainian.
 * @return string[] Array of terms to search (original + corrected).
 */
function panterrea_search_typo_variants(string $term): array
{
    $terms = [$term];

    // Collapse repeated consecutive characters (коррва → корова)
    $collapsed = preg_replace('/(.)\1+/u', '$1', $term);
    if ($collapsed !== $term && mb_strlen($collapsed) >= 2) {
        $terms[] = $collapsed;
    }

    return array_unique($terms);
}

/**
 * Expand a Ukrainian word to common grammatical forms for search.
 * Uses dictionary for known words, heuristic for others.
 *
 * @param string $term Base form (e.g. корова).
 * @return string[] All forms to search for.
 */
function panterrea_search_grammatical_forms(string $term): array
{
    $term = mb_strtolower(trim($term), 'UTF-8');
    $len = mb_strlen($term, 'UTF-8');

    if ($len < 2) {
        return [$term];
    }

    static $dictionary = null;
    if ($dictionary === null) {
        $dictionary = panterrea_get_ukrainian_search_dictionary();
    }

    if (isset($dictionary[$term])) {
        return $dictionary[$term];
    }

    return panterrea_search_heuristic_forms($term);
}

