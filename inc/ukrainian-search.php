/**
 * Heuristic expansion for words not in the dictionary.
 * Adds common Ukrainian noun/word endings.
 *
 * @param string $term Lowercased term.
 * @return string[]
 */
function panterrea_search_heuristic_forms(string $term): array
{
    $forms = [$term];
    $len = mb_strlen($term, 'UTF-8');
    $last = mb_substr($term, -1, 1, 'UTF-8');
    $stem = mb_substr($term, 0, -1, 'UTF-8');

    if ($len < 3) {
        return $forms;
    }

    // Feminine -а nouns: корова → корові, корови, корів, etc.
    if ($last === 'а' && mb_strlen($stem, 'UTF-8') >= 2) {
        $root = $stem;
        $forms = array_merge($forms, [
            $root . 'і',
            $root . 'и',
            $root . 'у',
            $root . 'ою',
            $root . 'ам',
            $root . 'ами',
            $root . 'ах',
        ]);
        // Genitive plural (e.g. корова→корів) has stem changes - use dictionary for accuracy
    }

    // Masculine -о or consonant: add common endings
    if ($last === 'о' || $last === 'е') {
        $forms = array_merge($forms, [
            $stem . 'а',
            $stem . 'у',
            $stem . 'ом',
            $stem . 'і',
            $stem . 'и',
            $stem . 'ів',
            $stem . 'ам',
            $stem . 'ами',
            $stem . 'ах',
        ]);
    }

    return array_unique(array_filter($forms, function ($f) {
        return mb_strlen($f, 'UTF-8') >= 2;
    }));
}

/**
 * Build expanded search terms: typo variants + grammatical forms.
 *
 * @param string $term Original search term.
 * @return string[] Flat list of terms to search (with LIKE %term%).
 */
function panterrea_search_expand_term(string $term): array
{
    $expanded = [];
    $typoVariants = panterrea_search_typo_variants($term);

    foreach ($typoVariants as $variant) {
        $forms = panterrea_search_grammatical_forms($variant);
        $expanded = array_merge($expanded, $forms);
    }

    return array_unique(array_filter($expanded, function ($t) {
        return is_string($t) && mb_strlen(trim($t), 'UTF-8') >= 2;
    }));
}

/**
 * Custom search SQL for catalog_post with Ukrainian support.
 * Replaces default WordPress search when querying catalog_post with search.
 *
 * @param string   $search Search SQL from WordPress.
 * @param WP_Query $query  The WP_Query instance.
 * @return string Modified search SQL.
 */
function panterrea_ukrainian_catalog_search(string $search, WP_Query $query): string
{
    global $wpdb;

    $s = $query->get('s');
    if (empty($s) || !is_string($s)) {
        return $search;
    }

    $postType = $query->get('post_type');
    $isCatalogSearch = in_array('catalog_post', (array) $postType, true);

    if (!$isCatalogSearch) {
        return $search;
    }

    $searchTerms = $query->get('search_terms');
    if (empty($searchTerms)) {
        $searchTerms = array_filter(preg_split('/\s+/u', trim($s)));
    }

    if (empty($searchTerms)) {
        return $search;
    }

    $termGroups = [];
    foreach ($searchTerms as $term) {
        $term = trim($term, "\"'");
        if (mb_strlen($term, 'UTF-8') < 2) {
            continue;
        }
        $expanded = panterrea_search_expand_term($term);
        if (!empty($expanded)) {
            $termGroups[] = $expanded;
        }
    }

    if (empty($termGroups)) {
        return $search;
    }

    $searchColumns = apply_filters('panterrea_search_columns', ['post_title', 'post_content'], $query);
    $searchColumns = array_intersect($searchColumns, ['post_title', 'post_excerpt', 'post_content']);
    if (empty($searchColumns)) {
        $searchColumns = ['post_title', 'post_content'];
    }

    $andGroups = [];
    foreach ($termGroups as $terms) {
        $orClauses = [];
        foreach ($terms as $term) {
            $like = '%' . $wpdb->esc_like($term) . '%';
            $parts = [];
            foreach ($searchColumns as $col) {
                $parts[] = $wpdb->prepare("({$wpdb->posts}.{$col} LIKE %s)", $like);
            }
            $orClauses[] = '(' . implode(' OR ', $parts) . ')';
        }
        $andGroups[] = '(' . implode(' OR ', $orClauses) . ')';
    }

    $searchSql = ' AND (' . implode(' AND ', $andGroups) . ') ';
    if (!is_user_logged_in()) {
        $searchSql .= " AND ({$wpdb->posts}.post_password = '') ";
    }

    return $searchSql;
}

add_filter('posts_search', 'panterrea_ukrainian_catalog_search', 20, 2);

/**
 * Get all search terms for highlighting (including grammatical forms).
 * Use when displaying search results to highlight matched words.
 *
 * @param string $searchString Raw search input from user.
 * @return string[] Terms to pass to highlight_search_query().
 */
function panterrea_search_get_highlight_terms(string $searchString): array
{
    $searchString = trim($searchString);
    if (mb_strlen($searchString, 'UTF-8') < 2) {
        return [];
    }

    $terms = array_filter(preg_split('/\s+/u', $searchString));
    $all = [];
    foreach ($terms as $t) {
        $all = array_merge($all, panterrea_search_expand_term($t));
    }
    return array_unique($all);
}